    for item in anos["anioFechaInicio"]:
        for ano in anos_selected:
            if item["nombreGrupo"] == ano:
                lista.append(
                    {"id": item["identificadorGrupo"], "ano": item["nombreGrupo"]}
                )
    lista.sort(key=lambda x: x.get("ano"), reverse=True)
    return lista


